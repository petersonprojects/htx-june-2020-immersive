####################### Integers and Floats ######################

print(5) # integer 
print(5.6) # float
# mixed type will be converted to a float
print(8 +  1.0) # result 9.0
print(8.0 / 3) # you get 2.666666

# Calculating with Python

print(20 + 3650)

print(8 * 3.57)

print(5 + 30 * 20)

# Use parentheses to control the order of operations

print(((5 + 30) * 20) / 10)

print(5 + 30 * 20 / 10)


